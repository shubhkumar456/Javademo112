package loops;

public class Forloops5 {

    public static void main(String[] args) {

        for (int i=0 ; i<10 ; i++){
            System.out.println("My name is Mr.Shubendra kumar"+" printing index :"+i);
        }

        for (int i=1; i<=10; i++){
            int var = 10;
            System.out.println("This is table ten"+var*i);
        }

        for (int i=1; i<=10; i++){
            int var = 33;
            System.out.println("Table of thirty three :"+var*i);
        }

        for (int i=10; i>0; i--){
            int var = 33;
            System.out.println("Table of thirty three :"+var*i);
        }

        for ( int i=10; i>0; i--){
            int var = 57;
            System.out.println("reverse table of fifty seven :"+var*i);
        }


    }
}
