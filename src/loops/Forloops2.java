package loops;

public class Forloops2 {
    public static void main(String[] args){
        for(int i=1; i<=10; i++){
            int thirteen=13;
            System.out.println("the table of thirteen:- "+ thirteen*i);
        }
        for(int i=1; i<=10; i++){
            int nineteen=19;
            System.out.println("this is the table of 19:-" +nineteen*i);
        }
        for(int i=1; i<=10; i++){
            int twentyseven= 27;
            System.out.println("the table of 27:-"+twentyseven*i);
        }
    }
}
