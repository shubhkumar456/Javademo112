package loops;

public class Loopsdecrement {
    public static void main(String[] args) {
        for (int i=10; i>0; i--){
            System.out.println("inception.........."+i);
        }
        for (int i=10; i>0; i--){
            int thirtyseven=37;
            System.out.println("decrement of table thirty seven...."+thirtyseven*i);
        }
        for (int i=10; i>0; i--){
            int thirtythree=33;
            System.out.println("another table decrement......"+thirtythree*i);
        }
        for (int i=10; i>0;i--){
            int nintseven=97;
            System.out.println("decrement of table nintyseven....."+nintseven*i);
        }


    }
}
