package loops;

public class Forloops4 {

    public static void main(String args[]) {


        for (int i =1; i <=10; i++) {
            int sixtyseven = 67;
            System.out.println("Lets see the print of table sixty seven  " +sixtyseven*i );
        }

        for (int i=1; i<=10; i++){
            int nintyseven = 97;
            System.out.println("let's have the print of table nseven  "+nintyseven*i);
        }

        for(double i=1; i<=10; i++){
            double eight = 8.1;
            System.out.println("See the diiferent table of eight in decimals"+eight*i);
        }

        for (int i=10; i>0;i--){
            int ten = 10;
            System.out.println("reverse of table  ten :"+ten*i);
        }

        for (double i=10; i>0; i--){
            double seventeen = 17.0;
            System.out.println("Decrement of table seventeen in decimals  :"+seventeen*i);
        }
    }
}

