package loops;

public class Forloops3 {


    public static void main(String[] args) {

        for(int i=0; i<10; i++){
            System.out.println("Welcome to the world of JAVA");
        }

        for (int i=1; i<=10; i++){

            int seventeen =17;
            System.out.println("Lets get the print of the table seventeen....:-"+seventeen*i);

        }

        for(double d=1; d<=10; d++){

            double nineteen = 19.1;
            System.out.println("Different mode of table nineteen...:-"+nineteen*d);
        }

        for (int i=10; i>0; i--){

            int fortyseven = 47;
            System.out.println("Reverse version of table fortysevn....-"+fortyseven*i);
        }

        for (int i=10; i>0; i--){

            int nintyone=91;
            System.out.println("The reverse table of nintyone...--"+nintyone*i);
        }

        for(double d=10; d>0; d--){

            double nintyseven = 97;
            System.out.println("Also the reverse version of table nintyseven...-"+nintyseven*d);
        }


    }
}
