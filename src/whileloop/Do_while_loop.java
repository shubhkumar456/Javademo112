package whileloop;

public class Do_while_loop {

    public static void main(String[] args) {

        int i = 0;

        do {
            System.out.println("Hello World" + "  Print index:-" + i);
            i++;
        }
        while (i < 10);

        int three = 3;
        int x = 1;
        do {
            System.out.println("Print Table three " + three * x);
            x++;
        }
        while (x <= 10);


        int fifteen = 15;
        int xx = 10;
        do {
            System.out.println("decrement of table fifteen :-" + fifteen * xx);
            xx--;
        }
        while (xx > 0);

        int seventeen = 17;
        int x1 = 1;
        do {
            System.out.println("Print table seventeen :-"+seventeen*x1);
            x1++;
        }
        while(x1<=10);
        }

    }



